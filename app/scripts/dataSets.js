
/* Data Sets -------------------------------------------------- */

function Triplet( x, y, z ) {
  this.x = x || 0;
  this.y = y || 0;
  this.z = z || 0;
}

function Dimension2d(w,h) {
  this.w = w || 100;
  this.h = h || 100;
}

function Dimension3d(w,h,l) {
  this.w = w || 100;
  this.h = h || 100;
  this.l = l || 100;
}
